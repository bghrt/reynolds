<h4><a href="http://library.vccs.edu/license-bin/linker.plx?xsreasy">EasyBib Premium </a></h4> 
<p>A web-based tool for building and managing citations in MLA, APA and Chicago Style. No login is required for on-campus access. Off-campus access requires you to login with your “MyReynolds” username and password.</p>

    <h4><a href="http://library.vccs.edu/license-bin/linker.plx?ebldda">eBook Library</a></h4>
 <p>Ebook Library (EBL) provides 2,300+ eBooks in the science, technology, engineering and math fields. Download or read in browser. Mobile friendly.
</p>

<h4><a href="http://library.vccs.edu/license-bin/linker.plx?ebsconlebk">eBooks on EBSCOhost</a></h4>      
<p>Provides access to over 19,000 full-text online books in a wide variety of subject areas.</p>

<h4><a href="http://library.vccs.edu/license-bin/linker.plx?cameconlit">EconLit  Icon</a></h4>
<p>American Economic Association's electronic bibliography of economic literature containing citations, with abstracts, to articles from more than 400 economics journals. In addition, subject indexing and abstracts of books and subject indexing of dissertation titles are included. </p>
    
     <h4><a href="http://library.vccs.edu/license-bin/linker.plx?ebscoagh">Economia y Negocios [en Español]</a> </h4> 
     <p>Covers business and economics. Collaborative effort between EBSCO Publishing and ESAN (Escuela de Administración de Negocios para Graduados). More than 70,000 records, dating back to the early 1980s, provide coverage for over 300 Spanish and Portuguese-language periodicals published in Argentina, Brazil, Chile, Colombia, Spain, the United States and elsewhere. (EBSCOhost)</p>
    
     <h4>
       <a href="http://ezproxy.vccs.edu:2048/login?url=http://library.vccs.edu/license-bin/linker.plx?ebscoehh">Education Research Complete </a></h4>
     <p>Online resource for education research. Topics covered include all levels of education from early childhood to higher education, and all educational specialties, such as multilingual education, health education, and testing. Education Research Complete provides indexing and abstracts for more than 1,840 journals, as well as full text for more than 950 journals, and includes full text for more than 81 books and monographs, and for numerous education-related conference papers. (EBSCOhost)</p>
       
       <h4><a href="http://library.vccs.edu/license-bin/linker.plx?camelectro">Electronics and Communications Abstracts </a></h4>
       <p>Includes information that ranges from medical, scientific and industrial applications in electronics to new technologies in communications. Theoretical and applied research summaries are provided as well as business and marketing aspects. (Cambridge Scientific Abstracts)</p>
       
       <h4><a href="http://library.vccs.edu/license-bin/linker.plx?camemabs">Engineered Materials Abstracts</a></h4>
      <p>Provides comprehensive coverage of the science, development and production of ceramics, polymers and composite materials. Over 1,600 sources including journals, technical reports, patents, dissertations, books and conference proceedings are indexed and abstracted. (Cambridge Scientific Abstracts)</p>
         
         <h4><a href="http://library.vccs.edu/license-bin/linker.plx?camemabs">English Poetry </a></h4> 
      <p>
           Contains the complete English poetic canon from Anglo-Saxon times to the end of the nineteenth century by writers from the British Isles. Overall the database contains over 165,000 poems drawn from about 4,500 separate printed sources by 1,257 named poets and includes many items by diverse anonymous hands. Every line by nearly every published poet can be searched. (Electronic Text Center, University of Virginia)</p>
           
           <h4><a href="http://library.vccs.edu/license-bin/linker.plx?etextevd">English Verse Drama</a></h4>
             
      <p>Contains more than 1,500 works by around 450 named authors and approximately 230 anonymous works, from the Shrewsbury Fragments of the late thirteenth century through the unparalleled output of the Elizabethan and Jacobean periods to the end of the nineteenth century. The completeness of the collection, as well as the complete author notes to each work, provides a unique context for the historical and linguistic study of English literature. (Electronic Text Center, University of Virginia)</p>
             
             <h4><a href="http://library.vccs.edu/license-bin/linker.plx?engbase">ENGnetBASE  </a></h4>
               
               <p>CRC Press is one of the world's leading publishers of engineering handbooks. Now the handbooks are available online. ENGnet BASE combines more than 160 top resources into one powerful research tool. (CRC Press)</p>
                 <h4><a href="http://ezproxy.vccs.edu:2048/login?url=http://library.vccs.edu/license-bin/linker.plx?ebscoeih">Environment Complete</a></h4>      
                 <p>Offers coverage in applicable areas of agriculture, ecosystem ecology, energy, renewable energy sources, natural resources, marine & freshwater science, geography, pollution & waste management, environmental technology, environmental law, public policy, social impacts, urban planning, and more.</p>
                 
                <h4> <a href="http://library.vccs.edu/license-bin/linker.plx?etextevd">Environmental Sciences & Pollution Management</a></h4>
                 
                <p> Multidisciplinary database, provides coverage of the environmental sciences. Abstracts and citations are drawn from over 6000 serials including scientific journals, conference proceedings, reports, monographs, books and government publications. (CSA Illumina / ProQuest)</p>
                 
                 <h4><a href="http://library.vccs.edu/license-bin/linker.plx?camemabs">ERIC</a></h4>   
                 
                 <p>This database is sponsored by the U.S. Department of Education to provide extensive access to education-related literature. ERIC provides coverage of journal articles, conferences, meetings, government documents, theses, dissertations, reports, audiovisual media, bibliographies, directories, books and monographs. (ProQuest)
                 
